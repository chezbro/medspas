- **Botox:** "Forehead lines fade by a shimmer—then she smiles confidently in a glowing spa lobby."  
- **Lip Fillers:** "A rose-gold ripple over a reflection—fuller, natural lips appear in a luxe treatment room."  
- **Laser Glow:** "A gentle beam sweeps across skin—revealing luminous, even tone in spa lighting."

These primes set emotional expectations before brand details appear.

