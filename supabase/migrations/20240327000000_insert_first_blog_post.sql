Why? Prospects who wait lose interest. Harvard Business Review found that contacting a lead within an hour boosts close rates by **60x** compared to a 24-hour wait ([dewy.io](https://www.dewy.io/blog/lead-conversion-best-practices-for-medical-aesthetic-practices?utm_source=chatgpt.com)).

